if ($_SERVER['REQUEST_METHOD'] === 'GET') {
    verify_webhook();
} elseif ($_SERVER['REQUEST_METHOD'] === 'POST') {
    handle_webhook();
} else {
    http_response_code(405);
    echo "Method not allowed";
}

